# using if else statements

# depending on what is the value of this variable is the output
alien_color = 'red'

if alien_color == 'green':
    print("You just earned 5 points!")
else:
    print("You just earned 10 points!")

# this version is green of the if statement will be true
alien_color = 'green'

if alien_color == 'green':
    print("You just earned 5 points!")
else:
    print("You just earned 10 points!")